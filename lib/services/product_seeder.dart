    const size = 400;
    final entries = _kDimData.entries.toList();
    for (int i = 0; i < entries.length; i += size) {
      final batch = _db.batch();
      final end = (i + size).clamp(0, entries.length);
      for (int j = i; j < end; j++) {
        final e = entries[j];
        final update = <String, dynamic>{};
        if (e.value.packQty != null) {
